class ApiError extends Error {
  isOperational
  statusCode
  constructor(
    statusCode,
    message,
    isOperational = false,
    stack = ''
  ) {
    super(message);
    this.statusCode = statusCode;
    this.isOperational = isOperational;
    if (stack) {
      this.stack = stack;
    } else {
      Error.captureStackTrace(this, this.constructor);
    }
  }
}

module.exports = ApiError;
